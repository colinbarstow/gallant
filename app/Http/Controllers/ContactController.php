     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $to = "colinbarstow@gmail.com";
        $number = $request['number'];
        $email = $request['email'];
        $name = $request['name'];
        $message = $request['message'];

        Mail::to($to)->send(new ContactMail($name, $message, $email, $number));

        Enquiry::create([
            'name' => $name,
            'email' => $email,
            'message' => $message,
            'number' => $number,
        ]);
        return back();
    }

